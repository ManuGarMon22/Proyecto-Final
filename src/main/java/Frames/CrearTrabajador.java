    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String datos[] = new String [3];
            datos[0]= (String) this.jComboBox1.getSelectedItem();
            datos[1]= this.jTextFieldNombre.getText();
            datos[2]=this.jTextFieldApellidos.getText();
            char[] contrasenia = this.contraseña1.getPassword();
            
        if (this.compararContraseñas()&& datos[0].equals("ADMINISTRADOR")){
            Trabajador t = CrearObjeto.trabajadorNuevo(datos, contrasenia);
            Listas.listaAdministradores.add(t);
            JOptionPane.showMessageDialog(null,"Administrador Creado con exito");
            dispose();
        }else if (this.compararContraseñas()&& (this.buscarPersonalExistente())){
                        
            Trabajador t = CrearObjeto.trabajadorNuevo(datos, contrasenia);
            
            if(datos[0] == "GERENTE DE AEROLINEA" ){
                Listas.listaGerentes.add(t);
                this.aero.setGerente(t);
                JOptionPane.showMessageDialog(null,"Gerente de Aerolinea creado con exito");
                dispose();
            }else if(datos[0] == "OPERADOR DE VUELOS"){
                Listas.listaOperadores.add(t);
                this.aero.setOperador(t);
                JOptionPane.showMessageDialog(null,"Operador de vuelos creado con exito");
                dispose();
            }  
        }else if(!this.compararContraseñas()){
            JOptionPane.showMessageDialog(null,"las contraseñas no coinciden, por favor, vuelva a intentarlo");
            this.contaseña2.setText("");
            this.contraseña1.setText("");
        }else if (!this.buscarPersonalExistente()){
             JOptionPane.showMessageDialog(null,"Ya existe un Operador de vuelo o Gerente de esta aerolinea, por favor intente con otra");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

