    public boolean compararContraseñas(){
        boolean x = true;
        char[] w =  this.contaseña2.getPassword();
        char[] r =  this.contraseña1.getPassword();
        
        for(int i = 0; i<this.contaseña2.getPassword().length;i++){
            if(w[i]!=r[i]){
                x = false;
                break;
            }
        }
            
        return x;
    }
    
    private boolean buscarPersonalExistente(){
        boolean x = false;
        if(this.jComboBox2.getSelectedItem() != null){
        String w = this.jComboBox2.getSelectedItem().toString();
        String y = (String)this.jComboBox1.getSelectedItem();
        
        for (Aerolinea a : Listas.listaAerolineas){
            
            if(w.equals(a.getNombre())){
                if(y.equals("GERENTE DE AEROLINEA")&& a.getGerente()==null){
                    this.aero = a;
                    x = true;
                    break;
                }else if (y.equals("OPERADOR DE VUELOS")&& a.getOperador()==null){
                    this.aero = a;
                    x = true;
                    break;
                }             
            }
        }
        }
        return x; 
    }   

